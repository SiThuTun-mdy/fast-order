create extension if not exists pgcrypto;

-- Application data --------------------------------------------------------

create table category (
  id         text primary key,        -- slug, matches existing frontend ids
  name       text not null,
  icon       text not null,
  sort_order integer not null default 0
);

-- Lookup tables in place of native Postgres ENUM types.
create table order_status (
  id         text primary key,   -- 'confirmed' | 'kitchen' | 'ready'
  label      text not null,      -- admin/introspection only; frontend keeps its own label map
  sort_order integer not null default 0
);

create table order_type (
  id    text primary key,        -- 'dine-in' | 'takeout'
  label text not null
);

create table menu (
  id          integer generated by default as identity primary key, -- seeded 1..15
  category_id text not null references category(id) on delete restrict,
  name        text not null,
  description text,
  unit_price  numeric(10,2) not null check (unit_price >= 0),
  emoji       text,
  popular     boolean not null default false,
  created_at  timestamptz not null default now()
);
create index menu_category_id_idx on menu(category_id);

create table customer (
  id     uuid primary key default gen_random_uuid(),
  name   text not null,
  email  text,
  mobile text
);

create table orders (
  id            text primary key,     -- 'ORD-<epoch-ms>', generated inside create_order()
  customer_id   uuid references customer(id) on delete set null,
  customer_name text not null,        -- denormalized snapshot
  phone         text,
  order_type    text not null references order_type(id),
  table_number  text,
  notes         text,
  subtotal      numeric(10,2) not null check (subtotal >= 0),
  tax           numeric(10,2) not null check (tax >= 0),
  total         numeric(10,2) not null check (total >= 0),
  status        text not null references order_status(id) default 'confirmed',
  created_at    timestamptz not null default now(),
  updated_at    timestamptz not null default now(),
  constraint dine_in_requires_table check (order_type <> 'dine-in' or table_number is not null)
);
create index orders_status_idx on orders(status);

create table order_item (
  id         bigint generated by default as identity primary key,
  order_id   text not null references orders(id) on delete cascade,
  menu_id    integer not null references menu(id) on delete restrict,
  name       text not null,      -- snapshot
  emoji      text,               -- snapshot
  unit_price numeric(10,2) not null check (unit_price >= 0),
  quantity   integer not null default 1 check (quantity > 0)
);
create index order_item_order_id_idx on order_item(order_id);

-- RBAC scaffolding (schema.md) — structural only, nothing reads/writes these yet

create table role (
  id        serial primary key,
  role_name text not null unique
);

create table app_user (
  id       uuid primary key default gen_random_uuid(),
  username text not null unique,
  password text not null
);

create table user_role (
  user_id uuid not null references app_user(id) on delete cascade,
  role_id integer not null references role(id) on delete cascade,
  primary key (user_id, role_id)
);

create table permission (
  id              serial primary key,
  permission_name text not null unique
);

create table role_permission (
  role_id       integer not null references role(id) on delete cascade,
  permission_id integer not null references permission(id) on delete cascade,
  primary key (role_id, permission_id)
);

