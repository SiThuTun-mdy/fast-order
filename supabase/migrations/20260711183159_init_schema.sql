-- Status transition enforcement ---------------------------------------------
-- order_status/order_type are lookup tables, not native enums, so the FK
-- only guarantees the value exists — this trigger is what actually
-- constrains the legal transition sequence.

create or replace function enforce_order_status_transition()
returns trigger language plpgsql as $$
begin
  if new.status is distinct from old.status then
    if not (
      (old.status = 'confirmed' and new.status = 'kitchen') or
      (old.status = 'kitchen'   and new.status = 'ready')
    ) then
      raise exception 'Invalid order status transition: % -> %', old.status, new.status;
    end if;
  end if;
  new.updated_at := now();
  return new;
end;
$$;

create trigger orders_enforce_status_transition
  before update on orders
  for each row execute function enforce_order_status_transition();

-- Atomic order creation -------------------------------------------------------

create or replace function create_order(
  p_customer_name text, p_phone text, p_order_type text,
  p_table_number text, p_notes text, p_items jsonb,
  p_subtotal numeric, p_tax numeric, p_total numeric
)
returns orders language plpgsql security invoker as $$
declare
  v_customer_id uuid;
  v_order_id    text := 'ORD-' || (floor(extract(epoch from clock_timestamp()) * 1000))::bigint;
  v_item        jsonb;
  v_order       orders;
begin
  insert into customer (name, mobile) values (p_customer_name, p_phone) returning id into v_customer_id;

  insert into orders (id, customer_id, customer_name, phone, order_type, table_number, notes, subtotal, tax, total)
  values (v_order_id, v_customer_id, p_customer_name, p_phone, p_order_type, p_table_number, p_notes, p_subtotal, p_tax, p_total)
  returning * into v_order;

  for v_item in select * from jsonb_array_elements(p_items) loop
    insert into order_item (order_id, menu_id, name, emoji, unit_price, quantity)
    values (v_order_id, (v_item->>'menu_id')::int, v_item->>'name', v_item->>'emoji',
            (v_item->>'price')::numeric, (v_item->>'quantity')::int);
  end loop;

  return v_order;
end;
$$;

grant execute on function create_order(text, text, text, text, text, jsonb, numeric, numeric, numeric)
  to anon, authenticated;
